package ArrayDemo;

public class SDArray {

	public static void main(String[] args) {
		String animals[]= {"Cat","Dog","Elephant","Lion","Tiger","Squirrel","Monkey","Mouse","Fox","Horse"};
		System.out.println(animals);
		for(int i=0;i<animals.length;i++)
		{
			System.out.println(animals[i]);
		}
			
		int num[]=new int[5];
		num[0]=15;
		num[1]=25;
		num[2]=35;
		num[3]=45;
		num[4]=55;
		for (int i=0;i<5;i++)
		{
			System.out.println(num[i]);
		
		}

	}

}


