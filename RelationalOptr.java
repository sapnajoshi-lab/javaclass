package operatorDemo;

public class RelationalOptr {

	public static void main(String[] args) {
		int x=21;
		int y=12;
		boolean res1=x>=y;
		System.out.println(res1);
		boolean res2=x==y;
		System.out.println(res2);
		boolean res3=x<=y;
		System.out.println(res3);
		boolean res4=x!=y;
		System.out.println(res4);
	}

}
